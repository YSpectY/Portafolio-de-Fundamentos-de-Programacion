# Miercoles es X
# Cada dia esta dado por su primera letra

Dia = input('Ingrese el Dia:')

if Dia == "L" or Dia == "l":
    print("Es Lunes")
elif Dia == "M" or Dia == "m":
    print("Es Martes")
elif Dia == "X" or Dia == "X":
    print("Es Miercoles")
elif Dia == "J" or Dia == "j":
    print("Es Jueves")
elif Dia == "V" or Dia == "v":
    print("Es Viernes")
elif Dia == "S" or Dia == "s":
    print("Es Sabado")
elif Dia == "D" or Dia == "d":
    print("Es Domingo")
else:
    print("Dia Erroneo")